# math模块保存了数学计算相关的方法，可以方便实现数学计算
import math
print(math.fabs(-100))   # 取绝对值
print(math.ceil(3.12))   # 向上取整
print(math.floor(9.99))  # 向下取整
print(math.pi)           #  π的值：3.141592653589793
print(math.pow(4,3))     # 计算某个数的平方，4的三次方
print(math.factorial(5))  # 表示计算数字的阶乘，5的阶乘 1 *２＊３＊４＊５
print(math.sin(89))